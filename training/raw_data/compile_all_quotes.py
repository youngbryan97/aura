def main():
    # 1. Compile the manual exact quotes
    compiled = []
    char_counts = {}
    for char, pairs in EXACT_QUOTES.items():
        char_counts[char] = len(pairs)
        for user_msg, assistant_msg in pairs:
            compiled.append({
                "user": user_msg,
                "assistant": assistant_msg,
                "source": char
            })

    # 2. Determine a healthy maximum cap for Ripley to prevent skew
    if len(char_counts) > 0:
        avg_quotes = sum(char_counts.values()) / len(char_counts)
    else:
        avg_quotes = 20
        
    ripley_cap = max(int(avg_quotes * 1.5), 50) 

    # 3. Load previously extracted Cornell-Ripley quotes and apply dynamic cap
    ripley_quotes = []
    try:
        with open('/Users/bryan/.aura/live-source/training/raw_data/verbatim_quotes_expanded.json', 'r') as f:
            data = json.load(f)
            # Filter to just the Cornell extractions
            all_ripley = [q for q in data if q.get("source") == "RIPLEY_CORNELL"]
            random.seed(42)
            ripley_quotes = random.sample(all_ripley, min(ripley_cap, len(all_ripley)))
    except Exception as e:
        print(f"Error loading ripley quotes: {e}")

    total_quotes = compiled + ripley_quotes
    
    print(f"Compiled {len(compiled)} exact manual quotes across {len(char_counts)} characters.")
    print(f"Average quotes per character: {avg_quotes:.1f}")
    print(f"Added {len(ripley_quotes)} Ripley Cornell quotes (Dynamic Cap applied).")
    print(f"Total Verbatim Dataset: {len(total_quotes)}")

    with open('/Users/bryan/.aura/live-source/training/raw_data/verbatim_quotes_final.json', 'w') as f:
        json.dump(total_quotes, f, indent=2)

if __name__ == "__main__":
    main()
